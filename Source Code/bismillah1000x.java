    private void show1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_show1ActionPerformed
        // TODO add your handling code here:
        jTextArea1.setText(null);
        String a = nav.getText();
        File file = new File (a);
        try{
            FileInputStream fs = new FileInputStream(file);
            DataInputStream in = new DataInputStream(fs);
            BufferedReader br = new BufferedReader(new InputStreamReader(in));
            String strline;
            while ((strline = br.readLine())!=null){
                StringTokenizer st = new StringTokenizer(strline,"");
                jTextArea1.setText(jTextArea1.getText()+st.nextToken()+ "\n");
            
            }
            in.close();
        }catch (Exception e){
             JOptionPane.showMessageDialog(this, "Choose a file and try again");
        }
    }//GEN-LAST:event_show1ActionPerformed

